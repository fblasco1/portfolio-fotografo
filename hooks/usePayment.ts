"use client";

import { useState } from 'react';
import { useRegion } from './useRegion';
import type { RegionInfo } from '@/lib/payment/region-detector';
import type { PaymentIntent, PaymentResult } from '@/lib/payment/payment-factory';

interface UsePaymentReturn {
  // Estados
  loading: boolean;
  error: string | null;
  paymentIntent: PaymentIntent | null;
  
  // Funciones
  createPaymentIntent: (items: any[], customerInfo?: any) => Promise<PaymentIntent | null>;
  processPayment: (paymentData: any) => Promise<PaymentResult>;
  clearError: () => void;
}

export function usePayment(): UsePaymentReturn {
  const { region } = useRegion();
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [paymentIntent, setPaymentIntent] = useState<PaymentIntent | null>(null);

  const createPaymentIntent = async (items: any[], customerInfo?: any): Promise<PaymentIntent | null> => {
    if (!region) {
      setError('Regi√≥n no detectada');
      return null;
    }

    setLoading(true);
    setError(null);

    try {
      console.log('üí≥ Creando sesi√≥n de pago...', {
        region: region.country,
        currency: region.currency,
        itemsCount: items.length
      });

      const response = await fetch('/api/payment/create-intent', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          region,
          items,
          customerInfo
        }),
      });

      const data = await response.json();

      if (!response.ok) {
        throw new Error(data.error || 'Error creando sesi√≥n de pago');
      }

      if (!data.success) {
        throw new Error(data.error || 'Error en la respuesta del servidor');
      }

      console.log('‚úÖ Sesi√≥n de pago creada:', data.paymentIntent);
      setPaymentIntent(data.paymentIntent);
      return data.paymentIntent;

    } catch (err: any) {
      const errorMessage = err.message || 'Error desconocido';
      console.error('‚ùå Error creando sesi√≥n de pago:', errorMessage);
      setError(errorMessage);
      return null;
    } finally {
      setLoading(false);
    }
  };

  const processPayment = async (paymentData: any): Promise<PaymentResult> => {
    if (!region) {
      return {
        success: false,
        error: 'Regi√≥n no detectada',
        status: 'failed',
        provider: 'unknown'
      };
    }

    setLoading(true);
    setError(null);

    try {
      console.log('üí≥ Procesando pago...', {
        region: region.country,
        provider: region.paymentProvider
      });

      const response = await fetch('/api/payment/process', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          region,
          paymentData
        }),
      });

      const data = await response.json();

      if (!response.ok) {
        throw new Error(data.error || 'Error procesando pago');
      }

      if (!data.success) {
        throw new Error(data.error || 'Error en la respuesta del servidor');
      }

      console.log('‚úÖ Pago procesado:', data.result);
      return data.result;

    } catch (err: any) {
      const errorMessage = err.message || 'Error desconocido';
      console.error('‚ùå Error procesando pago:', errorMessage);
      setError(errorMessage);
      
      return {
        success: false,
        error: errorMessage,
        status: 'failed',
        provider: region.paymentProvider
      };
    } finally {
      setLoading(false);
    }
  };

  const clearError = () => {
    setError(null);
  };

  return {
    loading,
    error,
    paymentIntent,
    createPaymentIntent,
    processPayment,
    clearError
  };
}

